// AskGroqCustom: ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏´‡∏•‡∏±‡∏Å‡∏ó‡∏µ‡πà‡∏¢‡∏≠‡∏°‡πÉ‡∏´‡πâ‡∏Å‡∏≥‡∏´‡∏ô‡∏î‡∏Ñ‡∏ß‡∏≤‡∏°‡∏¢‡∏≤‡∏ß‡∏Å‡∏≤‡∏£‡∏ï‡∏≠‡∏ö‡πÑ‡∏î‡πâ
func AskGroqCustom(prompt string, maxTokens int) string {
	startTime := time.Now()
	apiKey := os.Getenv("GROQ_API_KEY")
	if apiKey == "" {
		return "‡πÄ‡∏≠‡∏≠... ‡∏•‡∏∑‡∏°‡∏ï‡∏±‡πâ‡∏á GROQ_API_KEY ‡πÉ‡∏ô .env ‡∏ô‡∏∞‡∏ô‡∏≤‡∏¢"
	}

	url := "https://api.groq.com/openai/v1/chat/completions"
	payload := map[string]interface{}{
		"model":             "llama-3.3-70b-versatile",
		"messages":          []map[string]interface{}{{"role": "user", "content": prompt}},
		"temperature":       0.5,       // ‚úÖ ‡∏•‡∏î‡∏•‡∏á‡∏ô‡∏¥‡∏î‡∏´‡∏ô‡πà‡∏≠‡∏¢‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÉ‡∏´‡πâ‡∏ï‡∏≠‡∏ö‡∏ô‡∏¥‡πà‡∏á‡∏Ç‡∏∂‡πâ‡∏ô
		"max_tokens":        maxTokens, // ‚úÖ ‡∏õ‡∏£‡∏±‡∏ö‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏ï‡∏≤‡∏°‡∏Ñ‡∏ß‡∏≤‡∏°‡πÄ‡∏´‡∏°‡∏≤‡∏∞‡∏™‡∏°‡∏Ç‡∏≠‡∏á‡∏á‡∏≤‡∏ô
		"top_p":             0.8,
		"frequency_penalty": 0.6,
		"presence_penalty":  0.3,
	}

	jsonData, _ := json.Marshal(payload)
	req, _ := http.NewRequest("POST", url, bytes.NewBuffer(jsonData))
	req.Header.Set("Content-Type", "application/json")
	req.Header.Set("Authorization", "Bearer "+apiKey)

	client := &http.Client{Timeout: 15 * time.Second}
	resp, err := client.Do(req)
	if err != nil {
		return "Network Error ‡∏Å‡∏±‡∏ö Groq ‡∏à‡πâ‡∏≤"
	}
	defer resp.Body.Close()

	body, _ := io.ReadAll(resp.Body)
	var groqResp struct {
		Choices []struct {
			Message struct {
				Content string `json:"content"`
			} `json:"message"`
		} `json:"choices"`
	}

	if err := json.Unmarshal(body, &groqResp); err != nil || len(groqResp.Choices) == 0 {
		return "Groq ‡∏™‡∏≥‡∏•‡∏±‡∏Å‡∏à‡πâ‡∏≤ ‡∏•‡∏≠‡∏á‡πÉ‡∏´‡∏°‡πà‡∏ô‡∏∞"
	}

	result := strings.TrimSpace(groqResp.Choices[0].Message.Content)

	// üõ°Ô∏è ‡∏Å‡∏£‡∏≠‡∏á‡∏†‡∏≤‡∏©‡∏≤‡∏ï‡πà‡∏≤‡∏á‡∏î‡∏≤‡∏ß (Filter)
	if strings.Contains(result, "<|") || strings.Contains(result, "aff ") {
		return "‡∏≠‡∏∑‡∏≠‡∏´‡∏∑‡∏≠... ‡πÄ‡∏°‡∏∑‡πà‡∏≠‡∏Å‡∏µ‡πâ‡πÄ‡∏Ñ‡∏£‡∏∑‡πà‡∏≠‡∏á‡∏Ñ‡πâ‡∏≤‡∏á‡πÑ‡∏õ‡∏ô‡∏¥‡∏î ‡πÄ‡∏≠‡∏≤‡πÄ‡∏õ‡πá‡∏ô‡∏ß‡πà‡∏≤‡∏ñ‡∏≤‡∏°‡πÉ‡∏´‡∏°‡πà‡∏™‡∏¥!"
	}

	fmt.Printf("\n‚ö° [Groq Speed]: %v | Result Length: %d\n", time.Since(startTime), len(result))
	return result
}

