package handlers

import (
	"couple-app/services"
	"couple-app/utils"
	"encoding/json"
	"fmt"
	"net/http"
)

func HandleGetGangQuiz(w http.ResponseWriter, r *http.Request) {
	if utils.EnableCORS(&w, r) {
		return
	}

	category := r.URL.Query().Get("category")
	exclude := r.URL.Query().Get("exclude")

	// ✅ SUPER PROMPT ฉบับสมบูรณ์: บังคับ AI ให้เชี่ยวชาญทุกหมวดหมู่
	prompt := fmt.Sprintf(`ในฐานะมหาปราชญ์ผู้รอบรู้ระดับโลก จงสร้างคำถามในหมวด "%s" 
โดยมีเงื่อนไขเหล็ก:
1. ความยาวโจทย์: สั้นและทรงพลัง (ห้ามเกิน 15 คำไทย) ห้ามน้ำเยอะ
2. คุณภาพ: ต้องเป็นเกร็ดความรู้ระดับสากล (Global Context) ที่น่าทึ่งและแปลกใหม่
3. ความถูกต้อง: ข้อมูลต้องเป็น Fact จริง 100%% ห้ามมโนหรือใช้ทฤษฎีที่ยังไม่พิสูจน์
4. แนวทางการสร้างโจทย์ตามหมวดหมู่:
   - กีฬาและสถิติโลก: เน้นสถิติที่สุดยอดในโอลิมปิก, ฟุตบอลโลก หรือเหตุการณ์ประวัติศาสตร์ในวงการกีฬา
   - ภูมิศาสตร์โลก: เน้นสถานที่มหัศจรรย์, จุดที่ลึก/สูงที่สุด หรือปรากฏการณ์ธรรมชาติในแต่ละทวีป
   - เทคโนโลยี/AI: เน้นนวัตกรรมเปลี่ยนโลก, ประวัติศาสตร์คอมพิวเตอร์ หรือความลับของเทคโนโลยีปัจจุบัน
   - อาหารและวัฒนธรรม: เน้นที่มาของเมนูชื่อดังระดับสากล หรือประเพณีที่แปลกประหลาดทั่วโลก
   - สัตว์โลกน่ารัก: เน้นพฤติกรรมหรือความสามารถพิเศษของสัตว์ที่คนส่วนใหญ่ไม่เคยรู้
   - แบรนด์ดังระดับโลก: เน้นเบื้องหลังการก่อตั้งแบรนด์, โลโก้ลับ หรือสถิติยอดขายที่น่าตกใจ
   - วิทยาศาสตร์/อวกาศ: เน้นความลึกลับของจักรวาล, ฟิสิกส์ หรือการค้นพบทางชีววิทยาที่น่าเหลือเชื่อ
5. รายการคำถามที่เคยเล่นไปแล้ว (ห้ามสร้างซ้ำเด็ดขาด): [%s]

Return JSON ONLY: {"question": string, "options": [4 strings], "answer_index": 0-3, "sweet_comment": string}`, category, exclude)

	quiz, err := services.GenerateGangQuiz(prompt)
	if err != nil {
		http.Error(w, "AI Error: "+err.Error(), 500)
		return
	}

	w.Header().Set("Content-Type", "application/json")
	json.NewEncoder(w).Encode(quiz)
}
